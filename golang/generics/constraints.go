package main

import (
	"errors"
	"fmt"
	"time"
)

func chargeForLineItem[T lineItem](newItem T, oldItems []T, balance float64) ([]T, float64, error) {
	//setting the zero values
	var myZeroT []T
	var myZerofloat float64

	// Checking if user has enough funds
	if balance >= lineItem.GetCost(newItem) {
		oldItems = append(oldItems, newItem)
		newBalance := balance - lineItem.GetCost(newItem)
		return oldItems, newBalance, nil
	}
	
	// If balance is insufficient return zero values
	return myZeroT, myZerofloat, errors.New("insufficient funds")
}

